package gunpvp.data;

public class AdventureRush {
	
	private int world;
	private int steps;
	
	public AdventureRush(int world, int steps) {
		this.world = world;
		this.steps = steps;
	}

	public int getWorld() {
		return world;
	}

	public int getSteps() {
		return steps;
	}
	
}
